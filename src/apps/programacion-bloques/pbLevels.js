export function getLevels(level, grade) {
  const g = parseInt(grade, 10);
  if (level === 'primaria') return PRIMARIA[g] || [];
  if (level === 'eso') return ESO[g] || [];
  if (level === 'bachillerato') return BACH[g] || [];
  return [];
}

export function getRetos(level, grade) {
  const g = parseInt(grade, 10);
  if (level === 'primaria') return RETOS_PRIMARIA[g] || [];
  if (level === 'eso') return RETOS_ESO[g] || [];
  if (level === 'bachillerato') return RETOS_BACH[g] || [];
  return [];
}

export function checkLevel(level, result) {
  if (level.check) return !!level.check(result, level.world);
  if (result.error) return false;
  if (level.world.items && level.world.items.length > 0 && result.itemsCollected < level.world.items.length) return false;
  if (level.world.target && !result.onTarget) return false;
  return true;
}
